void DispColor(unsigned char data1,unsigned char data2)
{
	unsigned int i,j;

	BlockWrite(0,COL-1,0,ROW-1);

	SPI_CS = 0;
	
	spi_write(0x12);
	write_lcd(0x00);
	write_lcd(0x2C);
	write_lcd(0x00);


	for(i=0;i<ROW;i++)
	{	
		for(j=0;j<COL;j++)
		{
			write_lcd(data1);
			write_lcd(data2);
		}
	}	
	 
	SPI_CS = 1;
}


void frame()
{
	unsigned int i,j;

	BlockWrite(0,COL-1,0,ROW-1);

	SPI_CS = 0;
	
	spi_write(0x12);
	write_lcd(0x00);
	write_lcd(0x2C);
	write_lcd(0x00);


	for(i=0;i<ROW;i++)
	{	
		for(j=0;j<COL;j++)
		{
			if(i==0 || i==ROW-1||j==0||j==COL-1)
			{
				write_lcd(0xf8);
				write_lcd(0x00);
			}
			else
			{
				write_lcd(0x00);
				write_lcd(0x00);			
			}
		}
	}	
	 
	SPI_CS = 1;
}

void shizi_mark()
{
	unsigned int i,j;

	BlockWrite(0,COL-1,0,ROW-1);

	SPI_CS = 0;
	
	spi_write(0x12);
	write_lcd(0x00);
	write_lcd(0x2C);
	write_lcd(0x00);


	for(i=0;i<ROW;i++)
	{	
		for(j=0;j<COL;j++)
		{
			if(i==ROW/2-1 || i==ROW/2||j==COL/2-1||j==COL/2)
			{
				write_lcd(0xf8);
				write_lcd(0x00);
			}
			else
			{
				write_lcd(0xFF);
				write_lcd(0xFF);			
			}
		}
	}	
	 
	SPI_CS = 1;
}




void RGB(void)
{
	unsigned int i,j,k;
	unsigned char color[]={0xf8,0x00,0x07,0xe0,0x00,0x1f};

	BlockWrite(0,COL-1,0,ROW-1);

	SPI_CS = 0;
	
	spi_write(0x12);
	write_lcd(0x00);
	write_lcd(0x2C);
	write_lcd(0x00);

	for(i = 0; i<3; i++)
	{
		for(k=0;k<ROW/3;k++)
		{	
			for(j=0;j<COL;j++)
			{
				write_lcd(color[2*i]);
				write_lcd(color[2*i+1]);
			}
		}
	}	
	
		for(k=0;k<ROW%3;k++)
		{	
			for(j=0;j<COL;j++)
			{
				write_lcd(color[4]);
				write_lcd(color[5]);
			}
		}
	 
	SPI_CS = 1;
}





