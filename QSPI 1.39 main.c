void spi_write(unsigned char dat) //发送8bit
{
	unsigned char i;
	
	for(i=0;i<8;i++)
	{	
		if(dat&0x80)
		{	
			SIO0 = 1;	
		}
		else
		{	
			SIO0 = 0;	
		}
		
		dat <<= 1;
		
		SCL = 0; _nop_(); _nop_();_nop_();_nop_();
		SCL = 1;		
	}
}

void write_cmd(unsigned char cmd)
{	
	spi_write(0x02);
	spi_write(0x00);
	spi_write(cmd);
	spi_write(0x00);
	_nop_(); _nop_();		
}



void write_dat(unsigned char dat)
{	
	spi_write(dat);
}


void write_lcd(unsigned char dat)  //4-wire Pixel Write Data Waveform 
{
		SCL = 0;_nop_(); _nop_();	
	
		if(dat & 0x80){SI3 = 1;}		//bit7
		else					{SI3 = 0;}
		
		if(dat & 0x40){SI2 = 1;}		//bit6
		else					{SI2 = 0;}
		
		if(dat & 0x20){SI1 = 1;}		//bit5
		else					{SI1 = 0;}
	
		if(dat & 0x10){SIO0 = 1;}		//bit4
		else					{SIO0 = 0;}
		
		SCL = 1;_nop_(); _nop_();		

		

		
		SCL = 0;_nop_(); _nop_();	
		
		if(dat & 0x08){SI3 = 1;}		//bit3
		else					{SI3 = 0;}
		
		if(dat & 0x04){SI2 = 1;}		//bit2
		else					{SI2 = 0;}
		
		if(dat & 0x02){SI1 = 1;}		//bit1
		else					{SI1 = 0;}
	
		if(dat & 0x01){SIO0 = 1;}		//bit0
		else					{SIO0 = 0;}
		
		SCL = 1;_nop_(); _nop_();		
	
}

