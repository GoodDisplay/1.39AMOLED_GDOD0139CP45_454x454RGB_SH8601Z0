void gray16(void)	 
{
	unsigned int i,j,k;	
	unsigned char grayH8,grayL8;

	BlockWrite(0,COL-1,0,ROW-1);
	
	SPI_CS = 0;	

	spi_write(0x12);
	write_lcd(0x00);
	write_lcd(0x2C);
	write_lcd(0x00);
	
	for(i=0;i<ROW;i++)
	{
		for(j=0;j<COL%16;j++)
		{
			write_lcd(0x00);
			write_lcd(0x00);
		}

		// GRAY	
		for(j=0;j<16;j++)
		{
			grayH8=((j*2)<<3)|((j*4)>>3);		 
			grayL8=((j*4)<<5)|(j*2);
			
			for(k=0;k<COL/16;k++)
			{					
				write_lcd(grayH8);
				write_lcd(grayL8);
			}
		}
	}

	
	SPI_CS = 1;
	
}


void BlockWrite(unsigned int Xstart,unsigned int Xend,unsigned int Ystart,unsigned int Yend)
{
	
// 1.39	
//		Xstart += 12;
//	Xend += 12;

	
	//1.2
//		Xstart += 6;
//	Xend += 6;
	
	SPI_CS = 0;
		write_cmd(0x2a);   
		write_dat(Xstart>>8);
		write_dat(Xstart&0xff);
		write_dat(Xend>>8);
		write_dat(Xend&0xff);
	SPI_CS = 1;
	
	SPI_CS = 0;
		write_cmd(0x2b);   
		write_dat(Ystart>>8);
		write_dat(Ystart&0xff);
		write_dat(Yend>>8);
		write_dat(Yend&0xff);	
	SPI_CS = 1;
	
	SPI_CS = 0;
	write_cmd(0x2C);   
	SPI_CS = 1;
	
}







