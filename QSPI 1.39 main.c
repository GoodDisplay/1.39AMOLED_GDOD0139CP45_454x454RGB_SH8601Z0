/**********************************************************************
函数名：LCD_reset
功能：初始化前的复位
**********************************************************************/
void LCD_reset()
{
	RESET = 1; delayms(20);
	RESET = 0; delayms(20);
	RESET = 1; delayms(120);
}

/**********************************************************************
函数名：delayms
功能：延时毫秒
@24.000MHz: i = 24;j = 85;
@12.000MHz: i = 12;j = 169;
**********************************************************************/

void delayms(u16 ms)	
{
	unsigned char i, j;
	while(ms--)
	{
		i = 24;
		j = 85;
		do
		{
			while (--j);
		} while (--i);
	}
}

